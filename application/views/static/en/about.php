				just a single person hacking on a big messy block of code in their
				spare time, and is now a collection of people hacking on a big,
				not so messy block of code in their spare time. The new version
				of mozhunt no only runs on a completely rewritten codebase, but
				also has the advantage of multiple developers, a small group of
				marketeers, and an amazing artist.</p>
				
