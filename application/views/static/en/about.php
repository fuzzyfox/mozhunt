				<h1>History</h1>
				<h2>So what is the real story behind mozhunt?</h2>
				<p>Well mozhunt, (originally known as &ldquo;that easter egg hunt
				thing&rdquo;) was run by William &ldquo;FuzzyFox&rdquo; Duyck, a
				Mozilla community member. It was a volunteer project to help add
				some fun into the Mozilla community online, as well as get more
				information out about more things that were going on around the
				Mozilla ecosystem. The end result? A highly addictive game that
				made some people miss classes and sleep.</p>
				<p>Since the original version of the game the project has now been
				expanded several times. The first time round was a more refined
				idea and implementation of the same game, but this time run in
				conjunction with the Firefox 3.5 launch (still as a community run
				game). There was less interruption to the players which meant that
				people could spend more time looking for news, blogposts, and more
				around the Mozilla ecosystem that might be hiding the tokens used
				at the time (pandas), and less time filling in forms and clicking
				buttons.</p>
				<p>The name was gained a short while after the initial run as an
				easter egg hunt. It originated in the mind of one
				<a href="http://twitter.com/jruderman">Jesse Ruderman</a> as a nice
				little hashtag to use to help promote the game, and it just sort
				of stuck.</p>
				
				<h2>When was the last time this happened?</h2>
				<p>Well&hellip; it is hard to pin an exact date on it, due to the running 
				of some smaller instances of the game, however the last
				&ldquo;mozhunt&rdquo; run was in 2009 during the Firefox 3.5 release
				party season. It consisted of a total of 21 hidden pandas. The winner
				(going by the handle of jeff_themovie) was the winner with a
				respectable 13 pandas found. Jeff (as we shall now refer to him),
				was not left unrewarded either. He was lucy enough to be sent a
				one off, limited edition mozhunt t-shirt, along with a bag full of
				Mozilla goodies.</p>
				
				<h2>What&rsquo;s new since the last time?</h2>
